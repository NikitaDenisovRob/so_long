#include "libft.h"

void	ft_bzero(void *s, size_t p)
{
	size_t			t;
	unsigned char	*b;

	b = (unsigned char *)s;
	t = 0;
	while (t < p)
	{
		b[t] = 0;
		t++;
	}
}
